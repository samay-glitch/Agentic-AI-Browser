export function downloadAsFile(
  content: string,
  filename: string,
  type: string = 'text/markdown'
): void {
  const blob = new Blob([content], { type });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

export async function copyToClipboard(text: string): Promise<boolean> {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch {
    return false;
  }
}

export function summaryToEmailBody(summary: Summary): string {
  const lines: string[] = [];

  lines.push(`Summary: ${summary.metadata.title}`);
  lines.push('');
  lines.push(`Source: ${summary.metadata.url}`);
  lines.push('');
  lines.push('TL;DR');
  lines.push(summary.tldr);
  lines.push('');
  lines.push('Key Takeaways');
  for (const takeaway of summary.keyTakeaways) {
    lines.push(`  - ${takeaway}`);
  }
  lines.push('');
  lines.push('--');
  lines.push('Shared via Agentic AI Browser Assistant');

  return lines.join('\n');
}

export function openEmailWithSummary(summary: Summary): void {
  const subject = encodeURIComponent(`Summary: ${summary.metadata.title}`);
  const body = encodeURIComponent(summaryToEmailBody(summary));
  const mailtoUrl = `mailto:?subject=${subject}&body=${body}`;
  window.open(mailtoUrl);
}
